"""Listas en Python"""

"""Listas pop(): Quita un elemento de una posición dada"""

lista = ["Perú", "España", "Caanadá", "Aregentina"]

print("Mi lista inicial es la siguiente: {}".format(lista))

lista.pop()

print("Mi lista actualizada es la siguiente: {}".format(lista))

lista.pop(1)
print("Mi lista actualizada es la siguiente: {}".format(lista))