
"""Operadores aritméticos con Python"""

"""Operador potencia: **"""

potencia_1 = 10**3

print("El resultado de mi potencia_1 es: {}".format(potencia_1))

"""2da forma"""
"""Usando la palabra reservada pow podemos también elevar a un cierta potencia un número"""

"""Nos va a perdir dos valores el primero es la base y el segundo el valor al cuál vamos a elevar"""
potencia_2 = pow(20, 4)
potencia_3 = pow(4, 5)

print("El valor de mi potencia_2 es: {}".format(potencia_2))
print("El valor de mi potencia_3 es: {}".format(potencia_3))